package com.example.latihansqlitefaza.models;

public class PersonBean {

    private String name;
    private int age;

    public PersonBean() {

    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
